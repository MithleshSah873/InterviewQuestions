namespace OverlappingRectanglesQuestion.Problem
{
    public class Point
    {
        private int x;
        private int y;

        public int X { get { return x; } }
        public int Y { get { return y; } }

        public Point(int x1, int y1)
        {
            x = x1;
            y = y1;
        }
    }
}
